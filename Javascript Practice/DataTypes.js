//Primitive Data Types

// String
let name = 'Nisa';
console.log(name);

// Number
let num = 36;
console.log(num);

// Boolean
let isCoder = true;
console.log(isCoder);

// Undefined
let x;
console.log(x);

//Null
let y = null;
console.log(y);

// BigInt
let bigNum = 12345n;
console.log(bigNum);

// Symbol
let uniqueId = Symbol();
console.log(uniqueId);

// Non - Primitive DataTypes
let person = {
    name: "Nisa",
    id: 101
}


console.log(typeof name);
console.log(typeof id);
console.log(typeof person);
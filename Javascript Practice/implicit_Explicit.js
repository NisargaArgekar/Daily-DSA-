// implicit  Conversion
console.log('5' + 5);
console.log('5' - 2);
console.log('10' * '2');

//explicit Conversion

let str = "123";
let num = Number(str);
console.log(num + 10); //Converts String to Number

let boolVal = Boolean(0); // converts 0 to false
console.log(boolVal);

// Examples

console.log(Number("5") + 5);
console.log(String(5) + 5);
console.log(Boolean("Hello"));
console.log(Boolean(""));








